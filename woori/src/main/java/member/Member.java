package member;

public class Member {
	private int mbID;
	private String mbMeeting;
	private String mbUser;
	private String mbGreet;
	public int getMbID() {
		return mbID;
	}
	public void setMbID(int mbID) {
		this.mbID = mbID;
	}
	public String getMbMeeting() {
		return mbMeeting;
	}
	public void setMbMeeting(String mbMeeting) {
		this.mbMeeting = mbMeeting;
	}
	public String getMbUser() {
		return mbUser;
	}
	public void setMbUser(String mbUser) {
		this.mbUser = mbUser;
	}
	public String getMbGreet() {
		return mbGreet;
	}
	public void setMbGreet(String mbGreet) {
		this.mbGreet = mbGreet;
	}
	
}
