package review;

public class Review {
	private int rvID;
	private int rvRmeeting;
	private String rvUser;
	private String rvContent;
	private int rvAvailable;
	public int getRvID() {
		return rvID;
	}
	public void setRvID(int rvID) {
		this.rvID = rvID;
	}
	public int getRvRmeeting() {
		return rvRmeeting;
	}
	public void setRvRmeeting(int rvRmeeting) {
		this.rvRmeeting = rvRmeeting;
	}
	public String getRvUser() {
		return rvUser;
	}
	public void setRvUser(String rvUser) {
		this.rvUser = rvUser;
	}
	public String getRvContent() {
		return rvContent;
	}
	public void setRvContent(String rvContent) {
		this.rvContent = rvContent;
	}
	public int getRvAvailable() {
		return rvAvailable;
	}
	public void setRvAvailable(int rvAvailable) {
		this.rvAvailable = rvAvailable;
	}

}
