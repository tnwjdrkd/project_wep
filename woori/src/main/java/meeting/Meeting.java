package meeting;

public class Meeting {
	private int mtNum;
	
	private String mtID;
	private String mtAddress;
	private String mtCategory;
	private String mtLeader;
	private String mtSummary;
	private String mtDate;
	public int getMtNum() {
		return mtNum;
	}
	public void setMtNum(int mtNum) {
		this.mtNum = mtNum;
	}
	public String getMtID() {
		return mtID;
	}
	public void setMtID(String mtID) {
		this.mtID = mtID;
	}
	public String getMtAddress() {
		return mtAddress;
	}
	public void setMtAddress(String mtAddress) {
		this.mtAddress = mtAddress;
	}
	public String getMtCategory() {
		return mtCategory;
	}
	public void setMtCategory(String mtCategory) {
		this.mtCategory = mtCategory;
	}
	public String getMtLeader() {
		return mtLeader;
	}
	public void setMtLeader(String mtLeader) {
		this.mtLeader = mtLeader;
	}
	public String getMtSummary() {
		return mtSummary;
	}
	public void setMtSummary(String mtSummary) {
		this.mtSummary = mtSummary;
	}
	public String getMtDate() {
		return mtDate;
	}
	public void setMtDate(String mtDate) {
		this.mtDate = mtDate;
	}

}
